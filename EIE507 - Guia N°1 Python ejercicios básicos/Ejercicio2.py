import math #libreria math

r= int(input("Ingrese el radio: ")) # se pide el dato y se guarda e imprime por pantalla.
area = math.pi*(r*r) #Formula para calcular el radio de un círculo.
print("El área del círculo es {:.2f} unidades cuadradas.".format (area)) #El valor final es redondeado para una mejor lectura.